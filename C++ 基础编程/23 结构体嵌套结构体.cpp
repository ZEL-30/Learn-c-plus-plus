#include <iostream>
using namespace std;

//学生结构体
struct student 
{
	string name;
	int    age;
	int    scores;
};

// 定义老师的结构体
struct teacher
{
	int id;
	string name;
	int age;
	struct student stu;   //老师的结构体嵌套学生结构体
};



int main()
{
	teacher t;
	t.age = 62;
	t.name = "老王";
	t.id = 01;
	t.stu.age = 22;
	t.stu.name= "小明";
	t.stu.scores = 650;

	cout << t.stu.scores << endl;

	system("pause");

	return 0;
} 

#include <iostream>
using namespace std;


int main()
{
	float f1 = 3.14f;

	cout << "浮点数是：" << f1 << endl;

	double d1 = 3.1415926;

	cout << "浮点数是：" << d1<< endl;
	
	//统计内存空间
	cout << "float的内存空间：" << sizeof(f1) << endl;

	cout << "double的内存空间：" << sizeof(d1) << endl;



	//科学计数法
	float f2 = 9e3;  //9*10^3
	cout << "f2是：" << f2 << endl;

	float f3 = 3e-3; //3*0.1^3
	cout << "f3是：" << f3 << endl;





	system("pause");

	return 0;
}


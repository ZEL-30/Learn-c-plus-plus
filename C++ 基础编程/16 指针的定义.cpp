#include <iostream>
using namespace std;

int main()
{
	int a = 100;
	cout << "a = " << a << endl;
	int* pa = NULL;
	char* pb = NULL;
	pa = &a;
	*pa = 200;
	cout << "a的地址为：" << (long)pa << endl;
	cout << "a+1的地址为：" << (long)(pa + 1) << endl;
	cout << "a = " << a << endl;

	cout << "int* 所占的内存空间为：" << sizeof(pa) << endl;

	cout << "char* 所占的内存空间为：" << sizeof(pb) << endl;

	system("pause");

	return 0;
} 
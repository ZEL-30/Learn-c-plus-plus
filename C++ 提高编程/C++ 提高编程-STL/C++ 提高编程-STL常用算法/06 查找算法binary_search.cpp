void test01()
{
	vector<int> V;
	for (int i = 0; i < 10; i++)
	{
		V.push_back(i);
	}

	bool ret = binary_search(V.begin(), V.end(), 9);  //必须是无序序列

	if (ret)
	{
		cout << "找到了" << endl;
	}
}



